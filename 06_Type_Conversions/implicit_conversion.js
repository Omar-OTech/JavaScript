// String concatenation
console.log("3" + 4); // Outputs: "34"
console.log(3 + "4"); // Outputs: "34"
console.log("3" + true); // Outputs: "3true"

// Numeric conversion
console.log("6" - 2); // Outputs: 4
console.log("6" * "2"); // Outputs: 12

// Equality operators
console.log("5" == 5);       // true
console.log("5" === 5);      // false
